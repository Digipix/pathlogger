import UIKit

extension UIColor {
  class func backgroundDark() -> UIColor{
    return UIColor(rgba: "#364c66")
  }
  
  class func backgroundLight() -> UIColor {
    return UIColor(rgba: "#1c569b")
  }
  
  class func buttonWhite() -> UIColor {
    return UIColor(rgba: "#fc1a07")
  }
  
  class func buttonGrey() -> UIColor {
    return UIColor(rgba: "#d9d9d9")
  }
  
  class func buttonRed() -> UIColor {
    return UIColor(rgba: "#fc1a07")
  }
  
  class func buttonBlue() -> UIColor {
    return UIColor(rgba: "#226BAB")
  }
  
}


